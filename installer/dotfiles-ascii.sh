#!/usr/bin/env bash

BLUE=$(tput setaf 111)
RED=$(tput setaf 198)
BG_BLACK=$(tput setab 0)
BG_OTHER=$(tput setab 237)

tput sgr0
tput bold

cat <<'EOF' |\
    sed "s/[[:alnum:]\.\&\`\']/${BLUE}&/g" |\
    sed "s/[|_\/\\]/${RED}&/g" |\
    sed "1s/^/$BG_BLACK/" |\
    sed "2s/^/$BG_OTHER/"
 ____________________________________________________________________________
|                                                                            |
|                                                                            |
|                   88           .d88b.    00     8888                       |
|                  88            8Y  Y8    00       88     .d88b.   .d00b.   |
|   .d8b.         88             88                 88     8Y  Y8   8Y  Y8   |
|   8' Yb .8     88            8888888   8888       88     88  88   88.      |
|      'G8P'    88     .d88b.    88        88       88     888888   'G88b.   |
|              88     .888888.   88        88       88     88           Y8   |
|             88      '888888'   88        88       88     88  88   88  88   |
|            88        'G88P'    88      888888   888888   'G88P'   'G88P'   |
|                                                                            |
|____________________________________________________________________________|

EOF

tput sgr0



         
